const mongoose = require('mongoose');

// Define the Contact schema
const ContactSchema = new mongoose.Schema({
  name: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
  },
  contact: {
    type: String,
    required: true,
  },
  company: {
    type: String,
  },
  service: {
    type: String,
    required: true,
  },
  message: {
    type: String,
    required: true,
  },
  date: {
    type: Date,
    default: Date.now,
  },
});

// Create the Contact model
const Contact = mongoose.model('Contact', ContactSchema);

module.exports = Contact;
